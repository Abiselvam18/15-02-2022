package javaloops._02_2022;

public class Example1 
{
	public static void main( String[] args )
	{
		//for loop

		/**
		 * 
		 * for(initialization; condition; increment/decrement)
		 * {
		 * 		statements;
		 * }
		 * 
		 */
		
		for(int a=1; a<=10; a++)
		{
			System.out.println(a);
		}
		
	}
}
